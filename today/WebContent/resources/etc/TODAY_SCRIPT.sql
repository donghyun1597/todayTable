CREATE TABLE TB_MEMBER (
   MEM_NO NUMBER PRIMARY KEY,
   MEM_ID VARCHAR2(60) NOT NULL UNIQUE,
   MEM_PWD VARCHAR2(20) NOT NULL,
   NICKNAME VARCHAR2(60) NOT NULL UNIQUE,
   MEM_NAME VARCHAR2(20) NOT NULL,
   PHONE VARCHAR2(15) NOT NULL,
   WARNING_COUNT NUMBER DEFAULT 0,
   MEM_IMG VARCHAR2(300),
   MEM_STATUS CHAR(1) DEFAULT 'Y' CHECK (MEM_STATUS IN ('Y', 'N'))
);

CREATE TABLE TB_RECIPE (
        RECIPE_NO NUMBER CONSTRAINT RECIPE_PK PRIMARY KEY,
        RECIPE_NAME VARCHAR2(100) CONSTRAINT RECIPE_NM_NN NOT NULL,
        RECIPE_TAG VARCHAR2(100) CONSTRAINT RECIPE_TAG_NN NOT NULL,
        RECIPE_VIDEO VARCHAR2(300),
        RECIPE_DIFFICULTY CHAR(3) CHECK(RECIPE_DIFFICULTY IN ('상', '중', '하')) NOT NULL,
        RECIPE_PERSON NUMBER CONSTRAINT RECIPE_PERSON_NN NOT NULL,
        RECIPE_TIME NUMBER CONSTRAINT RECIPE_TIME_NN NOT NULL,
        RECIPE_DATE DATE DEFAULT SYSDATE NOT NULL,
        MEM_NO NUMBER REFERENCES TB_MEMBER ON DELETE CASCADE NOT NULL,
        RECIPE_PIC VARCHAR2(300) CONSTRAINT RECIPE_PIC_NN NOT NULL,
        RECIPE_VIEWS NUMBER DEFAULT 0 CONSTRAINT RECIPE_VIEWS_NN NOT NULL,
        RECIPE_STATUS CHAR(1) DEFAULT 'Y' CHECK(RECIPE_STATUS IN('Y', 'N')) NOT NULL
);
CREATE TABLE TB_ALLERGY (
   ALG_NO VARCHAR2(10) PRIMARY KEY,
   ALG_NAME VARCHAR2(50) NOT NULL
);
CREATE TABLE TB_WISHLIST (
	MEM_NO NUMBER REFERENCES TB_MEMBER ON DELETE CASCADE,
    RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
    CONSTRAINT WISHLIST_PK PRIMARY KEY(MEM_NO,RECIPE_NO)
);
CREATE TABLE TB_MEM_ALLERGY (
   MEM_NO NUMBER REFERENCES TB_MEMBER ON DELETE CASCADE,
   ALG_NO VARCHAR2(10) REFERENCES TB_ALLERGY ON DELETE CASCADE,
   CONSTRAINT ALLERGY_PK PRIMARY KEY(MEM_NO,ALG_NO)
);
CREATE TABLE TB_COOKING_ORDER (
    CO_NO NUMBER CONSTRAINT CO_NO_PK PRIMARY KEY,
    RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
    CO_STEP  VARCHAR2(10) CONSTRAINT CO_STEP_NN NOT NULL,
    CO_CONTENT VARCHAR2(300) CONSTRAINT CONTENT_NN NOT NULL
);
   
-- 테이블 생성(TB_INGRE_CLASS)
CREATE TABLE TB_INGRE_CLASS (
    INGRE_C_NO NUMBER PRIMARY KEY,
    RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
    INGRE_C_NAME VARCHAR2(50) DEFAULT '재료' NOT NULL
);

-- 테이블 생성(TB_INGREDIENT)
CREATE TABLE TB_INGREDIENT (
    INGRE_NO NUMBER PRIMARY KEY,
    INGRE_C_NO NUMBER REFERENCES TB_INGRE_CLASS,
    INGRE_NAME VARCHAR2(60) NOT NULL,
    INGRE_AMOUNT VARCHAR2(30) NOT NULL
);

-- 테이블 생성 (TB_COMMENT)
CREATE TABLE TB_COMMENT (
    COM_NO NUMBER CONSTRAINT COM_NO_PK PRIMARY KEY,
    RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
    COM_CONTENT VARCHAR2(300) CONSTRAINT COM_CONTENT_NN NOT NULL,
    MEM_NO NUMBER REFERENCES TB_MEMBER ON DELETE CASCADE NOT NULL,
    COM_DATE DATE DEFAULT SYSDATE,
    COM_STATUS CHAR(1) DEFAULT 'Y' CHECK (COM_STATUS IN ('Y', 'N'))
);
   
-- 테이블 생성(TB_REVIEW)
CREATE TABLE TB_REVIEW (
    REV_NO NUMBER CONSTRAINT REV_NO_PK PRIMARY KEY,
    RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
    REV_CONTENT VARCHAR2(1000) CONSTRAINT REV_CONTENT_NN NOT NULL,
    REV_IMG VARCHAR2(300) CONSTRAINT REV_IMG_NN NOT NULL,
    MEM_NO NUMBER REFERENCES TB_MEMBER ON DELETE CASCADE NOT NULL,
    REV_DATE DATE DEFAULT SYSDATE,
    REV_STATUS CHAR(1) DEFAULT 'Y' CHECK (REV_STATUS IN ('Y', 'N'))
);
   
-- 테이블 생성 (TB_COOKING_ORDER_IMG)
