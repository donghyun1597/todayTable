CREATE TABLE TB_CATEGORY (
   RECIPE_NO NUMBER REFERENCES TB_RECIPE ON DELETE CASCADE,
   C_NAT_NO VARCHAR2(10) REFERENCES TB_CATEGORY_NATION ON DELETE CASCADE,
   C_STEP_NO VARCHAR2(10) REFERENCES TB_CATEGORY_STEP ON DELETE CASCADE,
